export function getTranslator(locale: Locale) {
  const dict = messages[locale];
  return (key: string) => dict[key] ?? key;
}

export const I18nContext = createContext<I18n>({
  locale: "zh",
  t: getTranslator("zh"),
  setLocale: () => {},
});

export function useI18n() {
  return useContext(I18nContext);
}
